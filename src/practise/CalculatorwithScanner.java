package practise;

public class CalculatorwithScanner {
	
	public void doAddition(int a, int b) {
		int Add = a + b;
		System.out.println("Add :" +Add);
	}
	
	public void doSubstraction(int a, int b) {
		int Sub = a-b;
		System.out.println("Sub :" +Sub);
	}
	
	public void doMul(int a, int b) {
		int mul = a*b;
		System.out.println("Mul :" +mul);
	}
	
	public void doDiv(int a, int b) {
		int Div = a/b;
		System.out.println("Div :" +Div);
	}

}
