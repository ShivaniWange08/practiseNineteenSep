package practise;

public class DatatypesWithMethod {
	
	public int getInt() {
		return 10;
		}
	
	public short getShort() {
		return 200;
	}

	public long getLong() {
		return 123456789;
	}
	
	public byte getByte() {
		return 123;
	}
	
	public float getFloat() {
		return 1.34567f;
	}
	
	public double getDouble() {
		return 12.3345678;
	}
	
	public char getChar() {
		return 'S';
		
	}
	public boolean getBoolean() {
		return true;
	}
	
	public String getString() {
		return "Shivani";
	}
}
